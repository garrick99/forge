#!/bin/bash
# scripts/regen_parser.sh — regenerate the Menhir parser after grammar changes.
#
# Run this whenever you modify lib/parser/parser.mly.
# The generated parser.ml is checked into the repo; commit it along with parser.mly.
#
# Usage (from the forge root):
#   bash scripts/regen_parser.sh
#
# On Windows, run via WSL2:
#   wsl.exe -e bash -c "cd /mnt/c/Users/kraken/forge && bash scripts/regen_parser.sh"

set -e

FORGE_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
PARSER_DIR="$FORGE_ROOT/lib/parser"

echo "Forge root: $FORGE_ROOT"
echo "Parser dir: $PARSER_DIR"

# Check for menhir
if ! command -v menhir &>/dev/null; then
    echo "Error: menhir not found."
    echo "Install with: opam install menhir"
    exit 1
fi

echo "Menhir version: $(menhir --version)"

cd "$PARSER_DIR"

# Regenerate parser.ml from parser.mly
# --explain: generate a .conflicts file if there are shift/reduce conflicts
# --external-tokens: tokens are defined in the Token module (not inline in the grammar)
echo "Regenerating parser.ml from parser.mly..."
menhir --explain --external-tokens Token parser.mly

if [ $? -eq 0 ]; then
    echo "Done. parser.ml updated."
    echo ""
    echo "Next steps:"
    echo "  1. If you added new tokens, update lib/tokens/token.ml and lib/lexer/lexer.mll"
    echo "  2. Run: dune build"
    echo "  3. Run: bash test/run_all.sh"
    echo "  4. Commit both parser.mly AND parser.ml"
else
    echo ""
    echo "Menhir reported conflicts or errors."
    echo "Check parser.conflicts (generated by --explain) for details."
    echo ""
    echo "Common causes:"
    echo "  - Shift/reduce conflict: two grammar rules match the same token sequence"
    echo "  - Missing %prec declarations for operator precedence"
    echo "  - Ambiguous expression syntax (add parentheses to the grammar rule)"
    exit 1
fi
